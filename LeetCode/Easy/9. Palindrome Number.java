class Solution {
    public boolean isPalindrome(int x) {
        boolean check = true;
        String a = Integer.toString(x);
        for(int z = 0; z < a.length()/2; z++){
            if (a.charAt(z) != a.charAt(a.length()-z-1)){
                check = false;
            }
        }
        return check;
    }
}
