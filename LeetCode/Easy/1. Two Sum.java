class Solution {
    public int[] twoSum(int[] nums, int target) {
        int[] a = {0, 0};
        for(int x = 0; x < nums.length - 1; x++){
            for(int y = x + 1; y < nums.length; y++){
                if (nums[x] + nums[y] == target){
                    
                    a[0] = x;
                    a[1] = y;
                    
                }
            }
            
        }
        return a;
    }
}
